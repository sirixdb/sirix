package io.sirix.query;

import io.brackit.query.Query;
import io.brackit.query.compiler.translator.SequentialPipelineStrategy;
import io.sirix.JsonTestHelper;
import io.sirix.api.json.JsonResourceSession;
import io.sirix.index.projection.ProjectionIndexCatalog;
import io.sirix.index.projection.ProjectionIndexRegistry;
import io.sirix.query.json.BasicJsonDBStore;
import io.sirix.query.json.JsonDBCollection;
import io.sirix.query.scan.SirixVectorizedExecutor;
import org.junit.jupiter.api.AfterEach;
import org.junit.jupiter.api.Assertions;
import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;

import java.io.ByteArrayOutputStream;
import java.io.IOException;
import java.io.PrintWriter;

/**
 * THROWAWAY measurement harness (not meant for CI): quantifies the one cost the versioned
 * projection design pays — cold hydrate (decode all leaves on first touch of a
 * buildRevision) — against warm serving, plus the descriptor tier for contrast. Prints
 * timings with a [coldbench] prefix for extraction. Dataset: 2M deterministic records
 * generated by the session's scratchpad script, loaded via jn:load.
 */
public final class ProjectionColdHydrateBenchTest extends AbstractJsonTest {

  private static final String DATA_FILE =
      "file:///tmp/claude-0/-home-user-sirix/f8bb9c26-661a-5dd3-992b-67c218d274b8/scratchpad/bench2m.json";

  @BeforeEach
  public void clearBefore() {
    ProjectionIndexRegistry.clear();
    SequentialPipelineStrategy.setVectorizedExecutor(null);
  }

  @AfterEach
  public void clearAfter() {
    ProjectionIndexRegistry.clear();
    SequentialPipelineStrategy.setVectorizedExecutor(null);
  }

  @Test
  public void coldHydrateBench() throws IOException {
    // Bench-only harness: skip (never fail) on machines without the generated dataset.
    org.junit.jupiter.api.Assumptions.assumeTrue(
        java.nio.file.Files.exists(java.nio.file.Path.of(DATA_FILE.replace("file://", ""))),
        "bench dataset not present — skipping");
    long t = System.nanoTime();
    query("jn:load('json-path1','bench.jn','" + DATA_FILE + "')");
    System.out.println("[coldbench] shred_ms=" + ((System.nanoTime() - t) / 1_000_000));

    t = System.nanoTime();
    query("""
          let $doc := jn:doc('json-path1','bench.jn')
          let $stats := jn:create-projection-index($doc, '/[]',
              ('/[]/age', '/[]/active', '/[]/dept'),
              ('long', 'boolean', 'string'))
          return {"revision": sdb:commit($doc)}
        """);
    System.out.println("[coldbench] projection_build_ms=" + ((System.nanoTime() - t) / 1_000_000));

    ProjectionIndexRegistry.clear();
    ProjectionIndexCatalog.clearCache();

    try (final BasicJsonDBStore store =
            BasicJsonDBStore.newBuilder().location(JsonTestHelper.PATHS.PATH1.getFile().getParent()).build();
         final SirixQueryContext ctx = SirixQueryContext.createWithJsonStore(store);
         final SirixCompileChain chain = SirixCompileChain.createWithJsonStore(store)) {
      final JsonDBCollection collection = (JsonDBCollection) store.lookup("json-path1");
      final JsonResourceSession session = collection.getDatabase().beginResourceSession("bench.jn");
      final String resourceKey = session.getResourceConfig().getResource().toString();
      final int revision = session.getMostRecentRevisionNumber();
      final String[] root = { "[]" };

      // Descriptor tier, fully cold (probe + descriptor walk, zero segment loads).
      t = System.nanoTime();
      final long descCount = ProjectionIndexCatalog.countRowsFromDescriptors(
          session, resourceKey, revision, root);
      System.out.println("[coldbench] descriptor_count_cold_ms=" + ((System.nanoTime() - t) / 1_000_000)
          + " rows=" + descCount);
      Assertions.assertEquals(2_000_000L, descCount);

      // Full hydrate, cold (catalog caches cleared; descriptor walk above cached only
      // its own tier — DATA is still empty).
      Assertions.assertEquals(0L, ProjectionIndexCatalog.dataCacheSize());
      t = System.nanoTime();
      final ProjectionIndexRegistry.Handle handle = ProjectionIndexCatalog.lookupCovering(
          session, resourceKey, revision, root, new String[] { "age" });
      final long hydrateMs = (System.nanoTime() - t) / 1_000_000;
      Assertions.assertNotNull(handle);
      System.out.println("[coldbench] hydrate_cold_ms=" + hydrateMs
          + " leaves=" + handle.leafPayloads().size());
      // Hydrate again with everything OS/page warm to isolate steady re-decode cost.
      ProjectionIndexCatalog.clearCache();
      t = System.nanoTime();
      ProjectionIndexCatalog.lookupCovering(session, resourceKey, revision, root, new String[] { "age" });
      System.out.println("[coldbench] hydrate_rewarm_ms=" + ((System.nanoTime() - t) / 1_000_000));

      // Warm handle lookup (DATA hit).
      t = System.nanoTime();
      ProjectionIndexCatalog.lookupCovering(session, resourceKey, revision, root,
          new String[] { "age" });
      System.out.println("[coldbench] lookup_warm_us=" + ((System.nanoTime() - t) / 1_000));

      // Executor: cold first query (hydrate + kernel) vs warm repeats.
      ProjectionIndexRegistry.clear();
      ProjectionIndexCatalog.clearCache();
      final SirixVectorizedExecutor executor = new SirixVectorizedExecutor(session, revision, 4);
      SequentialPipelineStrategy.setVectorizedExecutor(executor);
      try {
        final String sumQuery = "let $doc := jn:doc('json-path1','bench.jn')\n"
            + "return sum(for $r in $doc[] return $r.age)";
        t = System.nanoTime();
        final String coldResult = evaluate(chain, ctx, sumQuery);
        System.out.println("[coldbench] first_query_cold_ms=" + ((System.nanoTime() - t) / 1_000_000));
        long best = Long.MAX_VALUE;
        String warmResult = null;
        for (int i = 0; i < 5; i++) {
          t = System.nanoTime();
          warmResult = evaluate(chain, ctx, sumQuery);
          best = Math.min(best, (System.nanoTime() - t) / 1_000);
        }
        System.out.println("[coldbench] warm_query_best_us=" + best);
        Assertions.assertEquals(coldResult, warmResult);

        final String filterQuery = "let $doc := jn:doc('json-path1','bench.jn')\n"
            + "return count(for $r in $doc[] where $r.age gt 40 and $r.active return $r)";
        t = System.nanoTime();
        evaluate(chain, ctx, filterQuery);
        System.out.println("[coldbench] filtered_count_first_ms=" + ((System.nanoTime() - t) / 1_000_000));
        best = Long.MAX_VALUE;
        for (int i = 0; i < 5; i++) {
          t = System.nanoTime();
          evaluate(chain, ctx, filterQuery);
          best = Math.min(best, (System.nanoTime() - t) / 1_000);
        }
        System.out.println("[coldbench] filtered_count_warm_best_us=" + best);
      } finally {
        SequentialPipelineStrategy.setVectorizedExecutor(null);
        executor.close();
      }
    }
  }

  private static String evaluate(final SirixCompileChain chain, final SirixQueryContext ctx,
      final String queryStr) throws IOException {
    try (final ByteArrayOutputStream out = new ByteArrayOutputStream();
         final PrintWriter printWriter = new PrintWriter(out)) {
      new Query(chain, queryStr).serialize(ctx, printWriter);
      printWriter.flush();
      return out.toString();
    }
  }
}
